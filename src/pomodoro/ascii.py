def big_ascii(s: str) -> list[str]:
    res: list[str] = ["" for _ in range(5)]
    for ch in s:
        if ch not in DIGITS:
            raise ValueError(f"`{ch}` is not allowed in `big_ascii()`")
        for i, row in enumerate(DIGITS[ch]):
            res[i] += row + " "
    return res


